<h1 style="margin-left:0">Filelinks Finder Plugin</h1>

<h2>Getting Started</h2>

<p>enable the plugin, it will be listed as Finder - Filelinks</p>

<p>the basic options are:
<ul>
<li>Search Limit</li>
<li>Search published off/on</li>
<li>Search archived off/on</li></ul>

<p>you need to have at least one of the options on.</p>

<p>The plugin will only display filelinks that the user is authorised to view and that match the extensions set in the options of the com_filelinks component.</p>
<p><strong>Note:</strong> this component is not yet complete. It does not take the component file extensions into account nor the viewing level when outputting an item</p>
<p>After installing the plugin open the zip archive and copy the contents folder com_finder to your template html folder</p>